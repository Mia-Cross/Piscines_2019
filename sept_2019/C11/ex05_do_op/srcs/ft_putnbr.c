#include <unistd.h>

void	ft_putnbr(int nb)
{
	char c;
	long p;

	if (nb < 0)
	{
		if (nb == -2147483648)
		{
			write(1, "-2147483648", 11);
			return ;
		}
		write(1, "-", 1);
		p = -1 * nb;
	}
	if (nb >= 0)
		p = nb;
	if (p > 9)
		ft_putnbr(p / 10);
	c = p % 10 + 48;
	write(1, &c, 1);
}
