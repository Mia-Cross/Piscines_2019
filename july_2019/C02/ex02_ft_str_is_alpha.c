int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((64 < str[i] && str[i] < 91) || (96 < str[i] && str[i] < 123))
			i++;
		else
			return (0);
	}
	return (1);
}
